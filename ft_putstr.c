#include"ft_printf.h"

int	ft_putstr(char *ch)
{
	int	i;

	i = 0;
	if (!ch)
	{
		ft_putstr("(null)");
		return (6);
	}
	while (ch[i] != '\0')
	{
		write(1, &ch[i], 1);
		i++;
	}
	return (i);
}
